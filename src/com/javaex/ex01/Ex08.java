package com.javaex.ex01;

public class Ex08 {

	public static void main(String[] args) {
		
		//관계연산자
		System.out.println("관계 연산자");
		
		int n1 = 5;
		int n2 = 3;
		
		System.out.println(n1==n2); //같음? 결과 false
		System.out.println(n1!=n2); //다름? 결과 true
		System.out.println(n1<n2); //n2가 더 큰가? false
		System.out.println(n1>n2); //n1이 더 큰가? true
		System.out.println(n1<=n2); //n2가 더 크거나 같나? false
		System.out.println(n1>=n2); //n1이 더 크거나 같나> true
		

		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
